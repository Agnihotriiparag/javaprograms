
/*
 Final Keyword : 
 |-- 1 variable
 |-- 2 method
 |-- 3 class
*/

// program showing the concept of final keyword

final class Demo1{ // here we make class final
    public static final void main(String args[]){ // here we make method final
        final int a = 100; // here we make variable constant
        System.out.println("value of a : "+a);
    }
}